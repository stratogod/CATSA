<b>1.	Should applicant complete an away rotation?</b>
<br /><br />

<u>Pros:</u>
<br />
<ul>
<li>Meet faculty and residents from programs you are interested in</li>
<li>Learn details about the institutions and programs</li>
<li>If a highly competitive program, being there would make you known to the program</li>
<li>May help you narrow your options</li>
</ul>
<br />


<u>Cons:</u>
<br />
<ul>
<li>Additional travel and housing expenses</li>
<li>Choosing programs may be difficult because there may be several that are appealing and it may not be possible to rotate at every institution you want to check out</li>
<li>Possibility of making a negative impression is just as likely as making a positive one</li>
</ul>
<br /><br /><br />


<a name="2"></a>
<b>2.	Are there selectives/electives that you should take to prepare for residency?</b>
<br /><br />

Typically students will participate in selectives/electives in the specialty they are interested in.  This would prepare the student for the specific specialty
