<h1>Letters of Recommendation (LoR)</h1>
<h2>Tips and Tricks</h2>

<br />

<b>Do's</b>
<br />
<ul>
<li>Letters should generally come from faculty who know you well, have worked with you in a clinical setting and are in the specialty you wish to pursue. </li>
<li>You want letters from the faculty that know you best and can describe your clinical abilities as well as your personal attributes, such as being a team player or having good problem solving skills. </li>
<li>You should get at least one letter, and probably two from a person in your chosen specialty. </li>
<li>You may also need to get a letter from the department chair and/or clerkship director. </li>
<li>Set up a face-to-face meeting so you can make a good impression and supply the necessary information they may need.</li>
<li>Anticipate who you will ask for letters, get to know them and let them get to know you beforehand.</li>
<li>Request a letter of recommendation directly and in person.</li>
<li>Ask if the faculty member would feel comfortable writing you a strong letter of support.</li>
<li>If they agree, follow-up with an email confirming so they will remember.</li>
<li>Plan to give your letter-writer at least four weeks notice for a letter</li>
<li>Provide a copy of your CV, personal statement, and evaluations.</li>
<li>Be as clear and specific as possible about deadlines and provide them with stamped, addressed envelopes. </li>
<li><a href="http://www.aamc.org/eras/" target="_blank">ERAS</a> has a letter request form/cover sheet that you can use to assist your letter-writer with format and submission. </li>
<li>Make sure you keep track of those whom you've asked and follow up to make sure your letters are being submitted. </li>
</ul>
<br /><br />

<b>Don'ts</b><br />
<ul>
<li>Generally you should not solicit letters from residents, preclinical faculty and non-physicians. </li>
<li>If after asking if you can count on a strong letter of support, the answer is anything other than a strong affirmative, you should probably look elsewhere.</li>
</ul>
