<h1>Interviews</h1>
<h2>Tips and Tricks</h2>
<br />
<b>What to do before the interview?</b>
<ul>
<li>Become familiar with the program
	<ul>
    	<li>Names of Chairman, Program Director, Administrators, and others</li>
        <li>Memorize important names!!!</li>
    </ul>
</li><br />

<li>What to wear?
	<ul>
    	<li>Professional suit, clean, neat, make sure it fits</li>
    </ul>
</li>
<br />

<li>Do you know where you are going?
	<ul>
    	<li>Map-it</li>
        <li>Have GPS updated</li>
        <li>Consider alternate routes</li>
        <li>Know the exact location where you should go</li>
        <li>Know where to park</li>
        <li>Call program ahead of time if you have any questions</li>
        <li>Consider a drive by the day before</li>
    </ul>
</li><br />

<li>Traveling?
	<ul>
    	<li>Confirm travel arrangements, schedules, lodging</li>
        <li>If driving – make sure car is in good working order</li>
        <li>Contingency plan in the event of inclement weather</li>
        <li>Arrive a day early if possible</li>
    </ul>
</li><br />

<li>What to Do Prior to the Interview?
	<ul>
    	<li>Review documents</li>
        <li>Review agenda for interview day</li>
        <li>Try to relax</li>
        <li>Get plenty of rest the night before</li>
        <li>Make sure your alarm is set and have a back-up</li>
    </ul>
</li>
<br />

<li>What to bring?
	<ul>
    	<li>C.V.</li>
    </ul>
</li><br />

<li>Interview Day
	<ul>
    	<li>Arrive early</li>
        <li>Prepare for rain or inclement weather, (rain, snow boots)</li>
    </ul>
</li><br />

<li>Interview
	<ul>
    	<li>Be confident</li>
        <li>Talk about your accomplishments</li>
        <li>Firm handshakes are good</li>
        <li>Eye contact!!! Important!!!</li>
        <li>Mind your manners – please and thank you</li>
        <li>Avoid repetitive gestures, phrases and uhh, ahh, ummms</li>
        <li>Focus</li>
        <li>Have a few questions in mind</li>
        <li>Be yourself and have a good attitude</li>
    </ul>
</li>
<br />

<li>After the interview day
	<ul>
    	<li>Drive around the city – impressions?
        	<ul>
            	<li>Likes, dislikes, could you live there?</li>
             </ul>
        <li>Compose thank you notes</li>
        <li>Did you like the program? Where (or) would you rank it?
        	<ul>
            	<li>Pros, cons</li>
            </ul>
         </li>
    </li>
  </ul>
</li><br />

<li>Meet with your adviser and debrief after</li>
</ul>


