<b>Addressing MSPE Committee</b>
<br />
Occasionally comments in clinical evaluations may cause some concern to the student.  Any such concerns may be brought to the MSPE committee's attention by the student.  In the "Submit MSPE" page there is a place to submit a note to the committee.  The comments will appear to the committee when they open your file for review. The note will not appear in the MSPE.  
<br /><br />
 
<b>Adviser Review required prior to submitting MSPE to committee</b>
