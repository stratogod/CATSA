<b>Getting Started</b><br />
<a href="https://www.aamc.org/students/medstudents/eras/residency/248548/gettingstarted.html" target="_blank">ERAS Token</a> can be obtained from Naomi Tucker in the OME.
<br /><br /><br />


<b>Registered</b><br />
<a href="https://services.aamc.org/eras/myeras" target="_blank">MyERAS</a>. It is important to register early. Your Designated Dean's Office cannot scan your documents until you have registered. After receiving your token from your Designated Dean's Office, log into MyERAS at <a href="https://services.aamc.org/eras/myeras" target="_blank">https://services.aamc.org/eras/myeras</a> and click Register to access the registration page. Carefully type in your Token ID number and fill out all required fields. You may only register once; therefore, enter all information and review it to ensure accuracy before submitting. 
<br /><br /><br />


<a name="2"></a>
<b>Photo</b><br />
Some of you might be on away rotations now or in the near future.  If you plan to apply to programs on September 15th when ERAS opens to submit applications, and have been unable to have your photo taken by then, you should still send in your application as soon as you are ready.  When you are able to have your photo taken, we'll send it to the programs.  Within ERAS, the photo is still an optional item, but programs do appreciate receiving it and it is to your advantage to provide the photo.  The photo is used for identification purposes, like when you arrive for your interview and when the program is creating their rank order list.  Again, don't delay sending in your application if you don't have your photo or any other of your documents such as LORs or med school transcript.  
<br /><br />
Here's the information for contacting the photographer:  Make an appointment by emailing <a href="mailto:LG@Recessinc.com">LG@Recessinc.com</a>; appointment days and times are:  Mondays 12:00-1:00 pm, and Tuesdays and Thursdays from 9-10 am.  If you are unable to schedule during these times, the photographer might be able to schedule outside these days and times.  The photographer will send your picture to the OME.

<br /><br /><br />


<a name="3"></a>
<b>Medical School Transcript</b><br />

You must request your official medical school transcript from the campus registrar's office.
<br />
<ul>
<li><b>MU Graduates</b> – submit written form – Specify <u>"Medical School Transcript ONLY"</u><br />You may use <a href="http://registrar.missouri.edu/forms/transcript-form.pdf" target="_blank">this form</a> (copies of this form are available in the OME and at the Transcript Office at 125 Jesse Hall)</li><br />
<li><b>Non MU Graduates</b> may request transcript on line <a href="https://musis1.missouri.edu/transcript/select.cfm" target="_blank">here</a></li>
</ul>
<br />
The transcript should sent to the OME, MA213 Medical Sciences Bldg. directly from the transcript office. 
<br /><br />
<u>It's recommended that you wait close to the time when you will apply to send through your request for your transcript as this allows as many grades as possible to be posted to your transcript. The transcript office usually has the transcript to the OME within 2-3 days.  Also, during the application cycle, if you have more grades added to your transcript, you can request another one (as long as you are willing to pay another $10) and we can re-scan and update your transcript on ERAS.</u>

<br /><br /><br />


<b>USMLE Transcript</b><br />
When designating how you want your USMLE transcript released to programs, there are 2 questions you must answer.  The first question is authorizing the release of your transcript to programs, and you want to answer "Yes."  The second question addresses whether you want to authorize automatic retransmission of your transcript.  The OME recommends that you do <u>not</u> choose the automatic retransmission, so you would answer "No" if you don't want automatic retransmission.  It is very easy to manually retransmit your USMLE transcript within ERAS whenever you receive new scores.
<br /><br /><br />



<b>Assigning Documents</b><br />
Your MyERAS application will go to all programs applied to.  You will assign your <a href="/status.php?step=5" target="_new">MSPE</a>, medical school transcript, <a href="/status.php?step=3" target="_new">personal statement(s)</a>, <a href="/status.php?step=4" target="_new">LoRs</a>, USMLE transcript, and photo to programs.  Once you assign a LoR to a program and apply, you won't be able to un-assign that specific LoR from those programs.  Basically, if you are applying to programs and then planning to ask for another letter (i.e., away rotation), you should leave an open LoR spot for a new letter.  If you end up not asking for another letter, you can always go back into ERAS and choose another letter to send.
<br /><br /><br />

<a name="4"></a>
<b>Application Certified</b><br/>
It is important to carefully review your application before certifying and submitting to ensure everything is complete and correct. You may only certify and submit your application once! 
<br /><br /><br />


<a name="7"></a>
<b>Monitor Programs</b><br />
<b>Document downloads via ADTS</b><br />
Applicant Document Tracking System (ADTS) details the status of documents submitted to the ERAS PostOffice. ADTS External Link lists each program to which the applicant has applied, the date each document was uploaded by the designated dean's office, and the date programs downloaded each document. Your designated dean's office also has access to ADTS , but programs do not.
