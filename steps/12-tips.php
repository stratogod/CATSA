<b>Creating your rank order list (ROL)</b><br />
Trying to decide your rank order list (ROL) for the Match? The best advice is also the most simple: rank all the programs at which you interviewed that you're willing to attend, in the order you most want to attend them. The match algorithm always favors applicants' ROL over residency programs'. So reach for those reach programs—as long as you also rank programs solidly within your reach. 
